//Difference between println and print
/*(1)print:-This will print statement in same line
/(2)println:-This will print statement in different line */
package com.company;

public class Prog2 {
    public static void main(String[] args)
    {
        //print
        System.out.print("Hello");
        System.out.print("Jaykumar");

        //println
        System.out.println("\nHello");
        System.out.println("Jaykumar");
    }
}
